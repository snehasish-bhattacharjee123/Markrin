export const validatePincode = (pincode) => {
  // Check if pincode is valid format (6 digits for India)
  const pincodeRegex = /^[1-9][0-9]{5}$/;
  return pincodeRegex.test(pincode);
};

export const getAddressFromPincode = async (pincode) => {
  // First check our local database
  if (PINCODE_DATABASE[pincode]) {
    return {
      success: true,
      data: PINCODE_DATABASE[pincode]
    };
  }

  // Try to fetch from API
  try {
    const response = await fetch(`${PINCODE_API_URL}/${pincode}`);
    const data = await response.json();
    
    if (data && data[0] && data[0].Status === 'Success' && data[0].PostOffice && data[0].PostOffice.length > 0) {
      const postOffice = data[0].PostOffice[0];
      return {
        success: true,
        data: {
          city: postOffice.District,
          state: postOffice.State,
          country: 'India'
        }
      };
    }
    
    return {
      success: false,
      error: 'Invalid pincode or no data found'
    };
  } catch (error) {
    console.error('Pincode API error:', error);
    return {
      success: false,
      error: 'Failed to validate pincode'
    };
  }
};

export default {
  validatePincode,
  getAddressFromPincode
};
